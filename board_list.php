<h3><strong>State Coordinator for Chapter Development</strong></h3>
[caption id="attachment_2815" align="alignleft" width="150"]<img src="https://aznow.org/wp-content/uploads/2018/12/CindyGuerra.png" alt="" width="150" height="150"> 
<strong>Cindy Guerra</strong>[/caption]
<br>Cindy Guerra (Inez Casiano/Central Phoenix chapter) has been a NOW member since 1982. She was President of the South Suburban NOW chapter; VP for Chapter Development, VP Action, and President of Illinois NOW. In addition to serving on the National Board and as Great Lakes Region Director to the Board, Cindy served on the Women of Color Committee while on the Board. She has been a feminist since she was 6 years old and will remain a NOW member forever to support women's rights and civil rights for all. Contact Cindy at <a href="mailto:Membership@aznow.org">Membership@aznow.org</a>.
</div>

