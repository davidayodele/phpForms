<h3><strong>State Coordinator for </strong><strong>Policy/Spokesperson </strong></h3>
[caption id="attachment_2380" align="alignleft" width="150"]<a href="https://aznow.org/about-az-now/about-az-now/sheila-ogea-scaled-120x150-2/"><img src="https://aznow.org/wp-content/uploads/2018/12/sheila-ogea-scaled-120x150.jpg" alt="Sheila Ogea" width="150" height="150"></a> <strong>Sheila Ogea</strong>[/caption]
<br><strong>Sheila Ogea</strong> (East Valley chapter) joined NOW in 1989 after attending an abortion rights rally. Married for almost 50 years to the same man, she has two grown daughters. Past experience includes a run for State Representative and newsletter editor for the Arizona Women’s Political Caucus. She has served two terms as a southwest regional representative on the National NOW Board and is now serving her second term as AZ NOW Policy/Spokesperson. Contact Sheila at <a href="mailto:PolicySpokesperson@aznow.org">PolicySpokesperson@aznow.org</a>.
</div>

