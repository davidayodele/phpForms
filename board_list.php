<h2>2019-2021 State Coordinators</h2>
<p>Our state coordinators are representatives from our different Arizona NOW chapters state-wide. For a description of the duties related to each position, please see the <a href="https://drive.google.com/file/d/0B4iRutogHnz_enA4ektVaDdWdXc/view?usp=sharingc/view?ths=true">AZ NOW Bylaws (rev 3-14-15)</a>.
</p>

