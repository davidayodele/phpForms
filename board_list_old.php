<h3 class="paragraph"><strong>State Coordinator for Legislative Action</strong></h3>
<div class="paragraph">
[caption id="attachment_2621" align="alignleft" width="140"]<a href="https://aznow.org/az-now-board/steele-victoria-headshot-2/" rel="attachment wp-att-2621"><img class="wp-image-2621" src="https://aznow.org/wp-content/uploads/2018/12/steele-victoria.jpg?w=278" alt="" width="140" height="140"></a> <strong>Victoria Steele</strong>[/caption]
<br><strong>Victoria Steele</strong> (Tucson chapter) is a lifelong feminist, former State Representative from District 9 in Tucson, mental health counselor, personal life coach, and a recent Congressional candidate. She enjoyed a 25-year career in radio and television news, and is an accomplished keynote speaker, teacher, and trainer who is experienced in presenting on Native American culture, empowering women, and a variety of public policy issues. Victoria is also the cofounder and immediate Past President of the Tucson NOW Chapter. Contact Victoria at <a href="mailto:LegislativeAction@aznow.org">LegislativeAction@aznow.org</a>.</div>
